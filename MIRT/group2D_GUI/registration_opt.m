% --- Executes just before registration_opt is made visible.
function registration_opt_OpeningFcn(hObject, eventdata, handles, varargin)
% This function has no output args, see OutputFcn.
% hObject    handle to figure
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
% varargin   command line arguments to registration_opt (see VARARGIN)

% Choose default command line output for registration_opt

handles.output=varargin{1};
% mesh window
% similarity
% number of levels
% regularization weight
% n of iterations
% local tolerance
% temporal step
% annealing rate
% global tolerance
% number of cycles

set(handles.edit1,'String', num2str(handles.output(1)));
set(handles.popupmenu1,'Value', handles.output(2));
set(handles.edit3,'String', num2str(handles.output(3)));
set(handles.edit4,'String', num2str(handles.output(4)));
set(handles.edit5,'String', num2str(handles.output(5)));
set(handles.edit6,'String', num2str(handles.output(6)));
set(handles.edit7,'String', num2str(handles.output(7)));
set(handles.edit8,'String', num2str(handles.output(8)));
set(handles.edit9,'String', num2str(handles.output(9)));
set(handles.edit10,'String', num2str(handles.output(10)));
set(handles.popupmenu3,'Value', handles.output(11));
set(handles.edit13,'String', num2str(handles.output(12)));



% Update handles structure
guidata(hObject, handles);


% Determine the position of the dialog - centered on the callback figure
% if available, else, centered on the screen
FigPos=get(0,'DefaultFigurePosition');
OldUnits = get(hObject, 'Units');
set(hObject, 'Units', 'pixels');
OldPos = get(hObject,'Position');
FigWidth = OldPos(3);
FigHeight = OldPos(4);
if isempty(gcbf)
    ScreenUnits=get(0,'Units');
    set(0,'Units','pixels');
    ScreenSize=get(0,'ScreenSize');
    set(0,'Units',ScreenUnits);

    FigPos(1)=1/2*(ScreenSize(3)-FigWidth);
    FigPos(2)=2/3*(ScreenSize(4)-FigHeight);
else
    GCBFOldUnits = get(gcbf,'Units');
    set(gcbf,'Units','pixels');
    GCBFPos = get(gcbf,'Position');
    set(gcbf,'Units',GCBFOldUnits);
    FigPos(1:2) = [(GCBFPos(1) + GCBFPos(3) / 2) - FigWidth / 2, ...
                   (GCBFPos(2) + GCBFPos(4) / 2) - FigHeight / 2];
end
FigPos(3:4)=[FigWidth FigHeight];
set(hObject, 'Position', FigPos);
set(hObject, 'Units', OldUnits);





% Make the GUI modal
set(handles.figure1,'WindowStyle','modal')

% UIWAIT makes registration_opt wait for user response (see UIRESUME)
uiwait(handles.figure1);

